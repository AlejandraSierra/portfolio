                En esta etapa, se realizó una búsqueda de <span class="fw-bold">findings e insights</span> a manera de posibles pensamientos frecuentes que podrían tener las personas que tienen o desean tener un huerto en casa, estos pensamientos fueron categorizados en <span class="fw-bold">clusters</span> que permitieron ver de manera más clara la información sobre los aspectos que debería abarcar la aplicación, posteriormente se generó un proceso <span class="fw-bold">in – out</span> haciendo un descarte inicial de funcionalidades y finalmente, a través de un <span class="fw-bold">diagrama MoSCoW</span>, se determinaron en orden de importancia cuales funcionalidades tenían que incluirse, cuáles deberían ser tenidas en cuenta, cuales podrían implementarse y cuales definitivamente no se iban a utilizar.<br><br>
                Con toda la información obtenida en las dos etapas previas, se procedió a crear una <span class="fw-bold">propuesta de valor</span> analizando y esquematizando de forma concisa los productos o servicios a ofrecer, los elementos creadores de valor, las soluciones que aportaría a los usuarios, así como sus ganancias, dificultades frecuentes y tareas que debería realizar al utilizar la aplicación, lo cual finalmente, se llevó a un <span class="fw-bold">modelo canvas</span> que permitió analizar la viabilidad del proyecto.<br><br>
