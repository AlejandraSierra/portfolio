                Con la información obtenida por los pasos previos, fue posible generar una <span class="fw-bold">matriz de utilidad y viabilidad</span> que permitió organizar las ideas obtenidas en cuanto a las posibles funcionalidades que podría tener la aplicación.<br><br>
