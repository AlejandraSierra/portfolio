        <div class="col-12 col-lg-7">
            <div class="text py-0 py-lg-2 ps-3 info-text">
                Para la ejecución de esta etapa se realizó la investigación y observación a partir de un <span class="fw-bold">desk research</span>, un proceso de búsqueda de información en medios electrónicos que permitió conocer las posibles motivaciones de las personas para tener un huerto en casa, conocer un poco más sobre el tema de alimentos transgénicos y orgánicos, y las posibilidades, ventajas y beneficios de los huertos urbanos. Posteriormente, se desarrolló una <span class="fw-bold">netnografía</span> o búsqueda de comentarios en la web de personas que han intentado tener o tienen un huerto en casa, con el fin de conocer sus opiniones sobre la experiencia que han tenido.<br><br>
                Con la información obtenida previamente, se plantearon unas <span class="fw-bold">research questions</span> usando el diagrama de Venn donde se han planteado varias preguntas, tomando como referencia diversas posibilidades y funcionalidades que se podrían implementar a la aplicación, luego, se hizo un <span class="fw-bold">benchmarking</span> que permitiera comparar los elementos incorporados en aplicaciones similares existentes en el mercado, un <span class="fw-bold">DAFO</span> que permitió identificar las principales debilidades, amenazas, fortalezas y oportunidades del proyecto, y finalmente, un <span class="fw-bold">cuestionario o encuesta</span> utilizando la herramienta Google Forms con preguntas segmentadas a personas que estarían dispuestas a tener un huerto y a quienes ya lo tienen para poder así conocer un poco sus expectativas, dificultades y comentarios al respecto.<br><br>
                Puedes ingresar a la documentación del proyecto <a href="../docs/documentacion_appio.pdf" target="_blank">haciendo clic aquí</a> para consultar con detalle los resultados del proceso de observación.
