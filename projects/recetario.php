                Teniendo en cuenta las etapas realizadas previamente, se realizó el <span class="fw-bold">proceso de desarrollo</span> llevando el prototipo a la web utilizando código en <span class="highlight-body">HTML5, CSS3, JavaScript incluyendo elementos de la librería jQuery</span>.<br><br>
                El sitio está cuenta con una <span class="fw-bold">página de inicio</span> siguiendo la estructura general de HTML: header, body y footer. En el header se puede ver una barra de navegación horizontal, un buscador y un banner hero; en el body hay un carrusel que muestra con íconos y texto las categorías de recetas disponibles, una sección de recetas populares, entradas destacadas y recientes del blog y un formulario de suscripción a la newsletter. En el footer, se pueden encontrar los enlaces a las redes sociales.<br><br>
                A la sección de “<span class="fw-bold">Recetas</span>” se puede acceder mediante el enlace que se encuentra en la barra de navegación, el carrusel de la página de inicio o el buscador de la web, en esta aparecerá un listado de recetas que pueden ser filtradas por el usuario para obtener unos resultados más acertados de acuerdo con sus necesidades de búsqueda. En esta vista se pretende mantener una interfaz lo más limpia posible, intentando no sobrecargarla de información.<br><br>
                Al hacer clic en alguna de las recetas, el usuario será redirigido a una vista detallada de las recetas, esta incluirá <span class="fw-bold">información básica</span> de la misma (nombre, categoría, descripción, tiempo, dificultad, entre otros), <span class="fw-bold">ingredientes necesarios</span> con la posibilidad de marcar aquellos que le hacen falta para poder añadirlos a una <span class="fw-bold">lista de compra</span> que posteriormente puede imprimir, adicionalmente cuenta con una calculadora que cambia la cantidad de cada ingrediente de acuerdo a las porciones que se deseen cocinar, <span class="fw-bold">utensilios</span> a utilizar, <span class="fw-bold">paso a paso</span> de cada receta chequeable, video tutorial de la receta y finalmente unas <span class="fw-bold">sugerencias de acompañamientos</span>.<br><br>
                En la sección “<span class="fw-bold">Blog</span>” aparecerán las entradas que se carguen al sitio web, en “<span class="fw-bold">Nosotros</span>” se incluirá información sobre el proyecto en general, los aliados, las motivaciones, entre otros. 
