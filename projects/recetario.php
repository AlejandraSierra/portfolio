            <div class="text py-0 py-lg-2 ps-3 info-text">
                En el proceso de research, se utilizó la metodología ágil <span class="fw-bold">Design Thinking</span> a partir de la generación de diversos “entregables” que permitieron organizar las ideas e información recolectada para llegar a conclusiones útiles y posibles funcionalidades que tendrá el sitio web. Este proceso incluye cuatro etapas: <span class="fw-bold">observación, síntesis, ideación y diseño</span>.<br><br>
                En la <span class="fw-bold">etapa de observación</span>, se generaron unas <span class="fw-bold">Research questions</span> en las que se tuvieron en cuenta posibles acciones o funcionalidades a las que podría acceder el usuario, con estas preguntas, se generó un <span class="fw-bold">How might we</span> que replantea la formulación de dichas preguntas determinar como se podrían ejecutar las funcionalidades para de acuerdo con las capacidades y recursos disponibles que posteriormente fueron categorizadas en:  <span class="highlight-body">funcionalidades, contenido y comunidad</span>. A partir de un <span class="fw-bold">Benchmarking</span>, se realizó una comparación de recetarios similares en la web con sus funcionalidades favorables e interfaces, luego, se hizo una <span class="fw-bold">Netnografía</span> que recoge opiniones en la web de personas que utilizan aplicaciones o sitios de recetas con el fin de conocer sus experiencias positivas y/o negativas; a continuación, se desarrollo un <span class="fw-bold">DAFO</span> en el cual se analizaron las posibles debilidades, amenazas, fortalezas y oportunidades del proyecto.<br><br>
                En la <span class="fw-bold">etapa de síntesis</span>, se realizó una <span class="fw-bold">matriz de utilidad y viabilidad</span> que permitió organizar de manera visual las posibles funcionalidades de acuerdo con su utilidad y viabilidad para así poder establecer su importancia y las posibilidades de crearlos de acuerdo con su dificultad.<br><br>
                En la <span class="fw-bold">etapa de ideación</span>, tomando como referencia la información obtenida en las etapas previas, se definieron las posibles funcionalidades que tendría el sitio web con unos <span class="fw-bold">finding e insights</span>, luego, a partir del uso de un esquema <span class="fw-bold">In – Out</span>, se aplicó un primer filtro para eliminar aquellas funcionalidades que por motivos técnicos o de gestión no se podían incorporar en una primera versión y finalmente, con el uso de un sistema <span class="fw-bold">MoSCoW</span>, se determinaron en orden de importancia cuales funcionalidades tenían que incluirse, cuáles deberían ser tenidas en cuenta, cuales podrían implementarse y cuales definitivamente no se iban a utilizar.<br><br>
                En la <span class="fw-bold">etapa de diseño</span> correspondiente al research, se realizó un <span class="fw-bold">Moodboard</span> o tablero de referentes e inspiración para conocer las últimas tendencias de diseño que podrían utilizarse en los elementos del recetario, posteriormente se generó una organización del contenido y un <span class="fw-bold">site map</span> que permitió plantear la estructura del sitio web con sus contenidos.<br><br>
